#' Synthetic RNA count matrix for runnable examples
#'
#' Tiny synthetic RNA-seq count matrix bundled so the user-cohort
#' workflow (`fit_stage_de` -> `classify_trajectory` ->
#' `assemble_user_evidence` -> `compute_audit_score`) can be exercised
#' end-to-end inside examples and the user-cohort vignette without an
#' external download. The first 10 rows (`TGENE01`-`TGENE10`) carry an
#' injected `Early_Burst_Up` signal (count lift of ~60-90 reads from
#' Stage I onward); the remaining 40 rows are negative-binomial noise.
#' Generated by `data-raw/build_toy_data.R` with `set.seed(2026)`.
#'
#' @format Integer matrix, 50 genes x 24 samples. Row names
#'   `TGENE01..TGENE50`; column names `S01..S24` matching
#'   `toy_coldata$sample`.
#' @keywords datasets
#' @examples
#' data("toy_counts", package = "pdactrace")
#' data("toy_coldata", package = "pdactrace")
#' if (requireNamespace("DESeq2", quietly = TRUE)) {
#'   fit <- fit_stage_de(toy_counts,
#'                       stage  = toy_coldata$stage,
#'                       cohort = toy_coldata$cohort)
#'   head(fit)
#' }
"toy_counts"

#' Synthetic stage / cohort metadata for runnable examples
#'
#' Sample-level metadata that pairs with `toy_counts` and
#' `toy_protein`. 24 samples balanced across the four canonical stages
#' and two synthetic cohorts.
#'
#' @format `data.frame` with 24 rows and 3 columns:
#'   * `sample` (character): `S01..S24`.
#'   * `stage`  (character): `"Normal"`, `"Early"`, `"Mid"`, `"Late"`,
#'     6 samples per stage.
#'   * `cohort` (character): `"CohortA"`, `"CohortB"`,
#'     12 samples per cohort.
#' @keywords datasets
"toy_coldata"

#' Public-data accession table backing list_data_sources()
#'
#' One row per public dataset that contributed to the v0.99.x
#' pdactrace reference atlas, covering bulk RNA-seq, tissue
#' proteomics, single-cell RNA, serum proteomics, pancreatitis
#' context, and validation cohorts. Backs the [list_data_sources()]
#' helper and is the source-of-truth for the manuscript's
#' data-availability section.
#'
#' @format `data.table` with one row per accession and columns
#'   `accession`, `layer`, `source_type`, `used_for`, `citation`,
#'   `url`. Generated by `data-raw/build_data_sources.R`.
#' @keywords datasets
"pdactrace_data_sources"

#' Per-stage tissue-protein effect-size table for trajectory plotting
#'
#' Bundled per-gene tissue-protein stage coefficients and 12-template
#' best-match labels, used by [plot_template_atlas()] and
#' [plot_gene_template()] for the protein layer (the main
#' `pdactrace_reference` only carries categorical `prot_pattern` /
#' `prot_tier`, not the per-stage betas needed for trajectory
#' visualisation). Generated by `data-raw/build_protein_betas.R`
#' from `phase34_protein_pooled_12template.csv` in the companion
#' manuscript-monorepo.
#'
#' @format `data.table`, 5,917 rows x 8 cols, keyed on `gene_symbol`:
#'   * `gene_symbol`     character HGNC symbol
#'   * `prot_beta_N`     numeric, always 0 (Normal is the reference)
#'   * `prot_beta_E`     numeric, log2 fold change vs Normal at Early
#'   * `prot_beta_M`     numeric, ditto at Mid
#'   * `prot_beta_L`     numeric, ditto at Late
#'   * `prot_pattern_12` character, full 12-template best-match
#'     (Early × 4 + Mid × 4 + Late × 2 + Monotonic × 2 +
#'     `"Unclassified"` for non-significant rows)
#'   * `prot_pattern_rho` numeric, Pearson rho with the matched
#'     template
#'   * `prot_lrt_padj`   numeric, BH-adjusted F-test p-value
#' @keywords datasets
"pdactrace_protein_betas"

